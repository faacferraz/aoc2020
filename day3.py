def day3p2(inp):
    input_list = inp.split('\n')
    slopes = [(1,1), (3,1), (5,1), (7,1), (1,2)]
    results = []
    n = len(input_list[0])

    for right, down in slopes:
        col = 0
        rt = 0
        for i, row in enumerate(input_list):
            if i%down:
                continue
            if row[col%n] == '#':
                rt += 1
            # print()
            # print(row)
            # print("{}{}{}".format(row[:col%n], 'O', row[col%n+1:]))
            # print("rt={}, col={}, n={}, col%n={}, row[col%n]={}".format(rt, col, n, col%n, row[col%n]))
            col += right
        results.append(rt)
    #print(results)
    rt = 1
    for r in results:
        rt *= r
    return rt
